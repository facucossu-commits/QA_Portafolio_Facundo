# Bug Report – Sitio web no responde al realizar búsqueda

**Descripción:**  
Al ingresar al sitio Google y realizar una búsqueda con la palabra “Wikipedia”, la página queda en blanco con un mensaje de error.  

**Pasos para reproducir:**  
1. Abrir https://www.google.com  
2. Escribir "Wikipedia" en el buscador  
3. Presionar Enter  

**Resultado esperado:**  
Mostrar resultados de búsqueda con link directo a Wikipedia.  

**Resultado actual:**  
Pantalla en blanco con mensaje de error.  

